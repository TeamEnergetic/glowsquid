package net.sw4t.glowsquid.enums;

/**
 * Represents a direction of rotation.
 */
public enum RotationDirection {
    /**
     * Clockwise, which is generally the direction the hands of a clock rotate in our universe when time
     * is flowing forwards.
     */
    CLOCKWISE,

    /**
     * Counter-clockwise, which is generally not the direction the hands of a clock rotate in our universe when time
     * is flowing forwards.
     *
     * Also known as anti-clockwise.
     */
    COUNTER_CLOCKWISE
}
